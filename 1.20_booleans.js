//Operators
//Will use these:
// > greater than
// < less than
// == equal to
// >= greater than or equal to
// <= less than or equal to
// && and
// || or
// ! not

var math = 10 > 5;
console.log(math);
//Can be combined with a string
console.log("Is ten bigger than five? " + math);

//Other operators to compare two things:
// && and
// || or
// ! not
console.log(math && 2>1);
console.log(10 != 5);

